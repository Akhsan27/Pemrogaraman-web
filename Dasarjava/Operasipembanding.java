package Dasarjava;
public class Operasipembanding {

    public static void main(String[] args) {
        int nilaiA = 30;
        int nilaiB = 10;
        boolean hasil;
        
        //apakah nilaiA lebih besar dari nilaiB
        hasil = nilaiA > nilaiB;
        System.out.println("NilaiA lebih besar dari nilaiB");
        System.out.println(hasil);
        //apakah nilaiA lebih kevil dari nilaiB
        hasil = nilaiA < nilaiB;
        System.out.println("NilaiA lebih kecil dari nilaiB");
        System.out.println(hasil);
        //apakah nilaiA lebih besar samadengan nilaiB
        hasil = nilaiA >= nilaiB;
        System.out.println("NilaiA lebih besar samadengan dari nilaiB");
        System.out.println(hasil);
        //apakah nilaiA lebih kecil samadengan nilaiB
        hasil = nilaiA <= nilaiB;
        System.out.println("NilaiA lebih kecil samadengan dari nilaiB");
        System.out.println(hasil);
        //apakah nilaiA samadengan nilaiB
        hasil = nilaiA == nilaiB;
        System.out.println("NilaiA samadengaan nilaiB");
        System.out.println(hasil);
        //apakah nilaiA tidak samadengan nilaiB
        hasil = nilaiA != nilaiB;
        System.out.println("NilaiA tidak samadengan nilaiB");
        System.out.println(hasil);





    }
}