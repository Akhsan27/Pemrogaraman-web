package Dasarjava;
public class DataTipe {

    public static void main(String[] args) {
       // tjenis tipe data
        String nama, alamat;
        int umur;
        double tinggi;
        // isi dari tipe data
        nama = "Muhammad Akhsanil Umam";
        alamat = "Langgngsari Kec.Cilongok Kab. Banyumas";
        umur = 21;
        tinggi = 160.5;
        // menampilkan isi tipe data
        System.out.println("Nama = " + nama);
        System.out.println("Alamat = " + alamat);
        System.out.println("Umur = " + umur);
        System.out.println("Tinggi = " + tinggi);
    }
}
/*
 * Tipe data :
 * > char = Tipe data karakter ex. z
 * > int = angka atau bilangan ex 19
 * > float = bilangan desimal ex. 2.3
 * > double = bilangan desimal juga tapi lebih besar kapasitasnya
 * > String = kumpulan dari karakter yang membentuk teks "HelloWorld"
 * > boolean = tipe data yang bernilai true dan false
 * FORMAT PENULISAN TIPE DATA
 * <tipedata> namaVariabel
 * ex. 
 * > variabel kosong bertipe integer ex. int namaVariabel;
 * > variabel bertipe integer dan langsung ada nilanya ex. int namaVariabel = 29;
 * > membuat sekumpulan variabel yang bertipe sama ex. int a, b, c, ;
 */